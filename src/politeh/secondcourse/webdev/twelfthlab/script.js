function openHistory() {
    // Получаем объект истории браузера
    var historyObject = window.history;

    // Проверяем, есть ли более 5 записей в истории
    if (historyObject.length > 5) {
        // Переходим ко второй странице в истории (индекс 1, так как индексация начинается с 0)
        historyObject.go(-1);
    } else {
        // Если менее 5 записей, просто выводим сообщение
        alert("История содержит менее 5 страниц.");
    }
}